document.addEventListener('DOMContentLoaded', () => {
    const langBtn = document.getElementById('langToggle');
    let currentLang = localStorage.getItem('lang') || 'es';

    // Function to update texts
    function updateLanguage(lang) {
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const keys = key.split('.');
            let text = translations[lang];

            keys.forEach(k => {
                if (text) text = text[k];
            });

            if (text) {
                element.innerHTML = text;
            }
        });

        // Update Button - show opposite language option
        if (lang === 'es') {
            langBtn.innerHTML = '<span class="lang-flag">üá∫üá∏</span><span class="lang-text">ENG</span>';
            langBtn.title = "Switch to English";
        } else {
            langBtn.innerHTML = '<span class="lang-flag">üá≤üáΩ</span><span class="lang-text">ESP</span>';
            langBtn.title = "Cambiar a Espa√±ol";
        }

        // Save preference
        localStorage.setItem('lang', lang);
        currentLang = lang;
    }

    // Initialize
    updateLanguage(currentLang);

    // Toggle listener
    if (langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.preventDefault();
            const newLang = currentLang === 'es' ? 'en' : 'es';
            updateLanguage(newLang);
        });
    }
});
